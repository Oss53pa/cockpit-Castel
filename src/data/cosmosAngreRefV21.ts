export function getJalonById(id: string): JalonV21 | undefined {
  return JALONS_V21.find(j => j.id === id);
}

export function getActionsByJalon(jalonId: string): ActionV21[] {
  return ACTIONS_V21.filter(a => a.jalonId === jalonId);
}

export function getJalonsByPhase(phase: ProjectPhase): JalonV21[] {
  return JALONS_V21.filter(j => j.phase === phase);
}

export function getActionsByPhase(phase: ProjectPhase): ActionV21[] {
  const phaseJalonIds = getJalonsByPhase(phase).map(j => j.id);
  return ACTIONS_V21.filter(a => phaseJalonIds.includes(a.jalonId));
}

export function getCriticalJalons(): JalonV21[] {
  return JALONS_V21.filter(j => j.niveau === 'critique');
}

export function getKeyDateJalons(): JalonV21[] {
  return JALONS_V21.filter(j => j.isKeyDate);
}

